
public class EjemploIterador 
{

	public static void main(String[] args) 
	{
		 String[] carros = {"Volvo", "BMW", "Ford", "Mazda"};
		
		 int arregloNum[]= {12, 23, 44, 56, 78};  
		
		 for(int i:arregloNum)
		 {  
		     System.out.println(i);  
		 }
		
		for (String i : carros) 
		{
		  System.out.println(i);
		}

	}

}
